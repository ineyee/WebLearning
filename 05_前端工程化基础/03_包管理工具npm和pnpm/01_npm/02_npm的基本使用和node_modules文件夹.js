// 3、安装完成后，npm会自动帮我们生成一个node_modules文件夹，里面就存放着所有包的源码，我们就可以导入使用了

/*
  补充：npm install和npm update的区别
  * npm install用来安装依赖，会根据package-lock.json文件里锁定的版本号来安装依赖
  * npm update用来更新依赖，会忽略package-lock.json文件里锁定的版本号，直接去package.json文件里把依赖更新到可用的最新版本并更新package-lock.json文件
*/